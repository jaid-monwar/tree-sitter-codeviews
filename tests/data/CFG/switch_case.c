// Switch-case statement
int main() {
    int day = 3;
    int result;

    switch (day) {
        case 1:
            result = 10;
            break;
        case 2:
            result = 20;
            break;
        case 3:
            result = 30;
            break;
        default:
            result = 0;
    }

    return result;
}
